<#
.SYNOPSIS
    Master reporting script that orchestrates IT data collection and generates
    a consolidated HTML dashboard report.

.DESCRIPTION
    This script acts as the central orchestrator for the PowerShell Toolkit. It:
    - Invokes Get-ADUserReport.ps1 to capture Active Directory user data
    - Invokes Get-SystemInventory.ps1 for each target computer
    - Combines all collected data into a single polished HTML report
    - Includes summary tables, color-coded status indicators, and charts (CSS-based)
    - Optionally emails the report to IT management

    The HTML report is self-contained (inline CSS, no external dependencies) and
    can be viewed in any modern browser or emailed directly.

.PARAMETER ADReportPath
    Path to an existing AD user report CSV (generated by Get-ADUserReport.ps1).
    If omitted, AD section is skipped.

.PARAMETER InventoryPath
    Path to a directory containing JSON inventory files (from Get-SystemInventory.ps1).
    If omitted, inventory section is skipped.

.PARAMETER TargetComputers
    Array of computer names to run inventory against directly. Alternative to
    providing pre-generated inventory files.

.PARAMETER HtmlOutput
    Path where the final HTML report will be saved.

.PARAMETER CompanyName
    Company name to display in the report header.

.PARAMETER SendEmail
    Switch to enable emailing the report after generation.

.PARAMETER SmtpServer
    SMTP server for email delivery (requires -SendEmail).

.PARAMETER MailFrom
    From address for the email (requires -SendEmail).

.PARAMETER MailTo
    Recipient address(es) for the email (requires -SendEmail).

.PARAMETER Credential
    Credential for remote inventory if -TargetComputers is used.

.EXAMPLE
    .\Invoke-ITReport.ps1 -ADReportPath "C:\Reports\AD_Users.csv" -InventoryPath "C:\Reports\Inventory" -HtmlOutput "C:\Reports\Dashboard.html"

    Generate a dashboard from existing AD and inventory data.

.EXAMPLE
    .\Invoke-ITReport.ps1 -TargetComputers "SRV-APP-01","SRV-DB-01","PC-WKS-045" -HtmlOutput "C:\Reports\Dashboard.html" -CompanyName "Contoso Ltd."

    Run inventory against three machines and generate a dashboard.

.EXAMPLE
    .\Invoke-ITReport.ps1 -ADReportPath "C:\Reports\AD_Users.csv" -InventoryPath "C:\Reports\Inventory" -HtmlOutput "C:\Reports\Dashboard.html" -SendEmail -SmtpServer "smtp.contoso.com" -MailFrom "it@contoso.com" -MailTo "manager@contoso.com"

    Generate the report and email it to management.

.NOTES
    Author: Louis Denis RAZAFIMANDIMBY
    Requires: PowerShell 5.1+, output from Get-ADUserReport and Get-SystemInventory
    Version: 1.2
#>

[CmdletBinding()]
param(
    [Parameter(Mandatory = $false)]
    [ValidateScript({ Test-Path $_ -PathType Leaf })]
    [string]$ADReportPath,

    [Parameter(Mandatory = $false)]
    [ValidateScript({ Test-Path $_ -PathType Container })]
    [string]$InventoryPath,

    [Parameter(Mandatory = $false)]
    [string[]]$TargetComputers,

    [Parameter(Mandatory = $true, HelpMessage = "Full path to the output HTML report")]
    [ValidateNotNullOrEmpty()]
    [string]$HtmlOutput,

    [Parameter(Mandatory = $false)]
    [string]$CompanyName = "IT Department",

    [Parameter(Mandatory = $false)]
    [switch]$SendEmail,

    [Parameter(Mandatory = $false)]
    [string]$SmtpServer,

    [Parameter(Mandatory = $false)]
    [string]$MailFrom,

    [Parameter(Mandatory = $false)]
    [string[]]$MailTo,

    [Parameter(Mandatory = $false)]
    [PSCredential]$Credential
)

#region Functions

function Write-LogMsg {
    param(
        [string]$Message,
        [ValidateSet('INFO', 'WARN', 'ERROR', 'OK')]
        [string]$Level = 'INFO'
    )
    $ts = Get-Date -Format "yyyy-MM-dd HH:mm:ss"
    $entry = "[$ts] [$Level] $Message"
    switch ($Level) {
        'ERROR' { Write-Host $entry -ForegroundColor Red }
        'WARN'  { Write-Host $entry -ForegroundColor Yellow }
        'OK'    { Write-Host $entry -ForegroundColor Green }
        default { Write-Host $entry -ForegroundColor Gray }
    }
}

