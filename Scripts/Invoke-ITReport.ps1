if ($adData) {
    Write-Host "  AD Users                    : $($adData.Count)" -ForegroundColor White
    Write-Host "  Problem accounts            : $($problemAccounts.Count)" -ForegroundColor $(if($problemAccounts.Count -gt 0){'Yellow'}else{'Green'})
}
Write-Host "  Machines inventoried        : $($inventoryData.Count)" -ForegroundColor White
Write-Host "  HTML report                 : $HtmlOutput" -ForegroundColor White
Write-Host "  Log file                    : $logPath" -ForegroundColor White
Write-Host "==============================================" -ForegroundColor Cyan
Write-Host ""

Stop-Transcript
Write-LogMsg -Level OK -Message "Script completed successfully"

