# AD KPIs
if ($adData) {
    $disabledUsers = @($adData | Where-Object { $_.Enabled -eq 'False' -or $_.AccountStatus -match 'Disabled' }).Count
    $lockedUsers = @($adData | Where-Object { $_.LockedOut -eq 'True' -or $_.AccountStatus -match 'Locked' }).Count
    $inactiveUsers = @($adData | Where-Object { $_."DaysSinceLastLogon" -ne 'N/A' -and [int]$_."DaysSinceLastLogon" -gt 90 }).Count

    $kpiHTML += @"
    <div class="kpi-card blue">
        <div class="label">Total AD Users</div>
        <div class="value blue">$totalUsers</div>
    </div>
    <div class="kpi-card orange">
        <div class="label">Disabled Accounts</div>
        <div class="value orange">$disabledUsers</div>
    </div>
    <div class="kpi-card red">
        <div class="label">Locked Accounts</div>
        <div class="value red">$lockedUsers</div>
    </div>
    <div class="kpi-card orange">
        <div class="label">Inactive (90d+)</div>
        <div class="value orange">$inactiveUsers</div>
    </div>
"@
}

# Machine KPIs
if ($inventoryData.Count -gt 0) {
    $diskAlerts = 0
    $totalRAM = 0
    $osCounts = @{}

    foreach ($machine in $inventoryData) {
        if ($machine.OperatingSystem) {
            $osName = $machine.OperatingSystem.Caption
            if ($osName) {
                $osCounts[$osName] = [int]$osCounts[$osName] + 1
            }
        }
        if ($machine.Hardware) {
            $totalRAM += $machine.Hardware.TotalRAMGB
        }
        if ($machine.LogicalDisks) {
            $diskAlerts += @($machine.LogicalDisks | Where-Object { $_.Alert -ne 'OK' }).Count
        }
    }

    $kpiHTML += @"
    <div class="kpi-card purple">
        <div class="label">Total Machines</div>
        <div class="value">$totalMachines</div>
    </div>
