        <div class="label">Total RAM</div>
        <div class="value blue">$([math]::Round($totalRAM, 0)) GB</div>
    </div>
    <div class="kpi-card $(if($diskAlerts -gt 0){'red'}else{'green'})">
        <div class="label">Disk Alerts</div>
        <div class="value $(if($diskAlerts -gt 0){'red'}else{'green'})">$diskAlerts</div>
    </div>
"@
}

$kpiHTML += '</div>'
[void]$html.Append($kpiHTML)

# ───── AD User Table ─────
if ($adData) {
    [void]$html.Append('<h2><i class="icon">&#128100;</i> Active Directory — User Report</h2>')

    # Summary of disabled/locked
    $problemAccounts = @($adData | Where-Object { $_.AccountStatus -ne 'Active' })
    if ($problemAccounts.Count -gt 0) {
        [void]$html.Append(@"
<div class="status-grid">
    <div class="status-card" style="border-left: 4px solid #ff9800;">
        <h4>Accounts Needing Attention</h4>
        <div class="detail">Total issues: $($problemAccounts.Count) / $totalUsers</div>
    </div>
</div>
"@)
    }

    [void]$html.Append('<div style="overflow-x: auto;"><table>')
    [void]$html.Append(@"
<thead><tr>
    <th>Name</th>
    <th>Username</th>
    <th>Email</th>
    <th>Department</th>
    <th>Title</th>
    <th>Status</th>
    <th>Last Logon</th>
    <th>Days Since Logon</th>
</tr></thead>
<tbody>
"@)

    # Show all users or at most 500 in the table
    $displayUsers = $adData | Sort-Object Name
    $truncated = $false
    if ($displayUsers.Count -gt 500) {
        $displayUsers = $displayUsers | Select-Object -First 500
        $truncated = $true
    }

    foreach ($user in $displayUsers) {
        $rowBg = if ($user.AccountStatus -ne 'Active') { ' style="background-color:#fff3e0;"' } else { '' }
        [void]$html.Append("<tr$rowBg>")
        [void]$html.Append("<td>$($user.Name)</td>")
        [void]$html.Append("<td>$($user.SamAccountName)</td>")
        [void]$html.Append("<td>$($user.Email)</td>")
        [void]$html.Append("<td>$($user.Department)</td>")
        [void]$html.Append("<td>$($user.Title)</td>")
        [void]$html.Append("<td>$((Get-StatusBadge -Status $user.AccountStatus))</td>")
        [void]$html.Append("<td>$($user.LastLogonDate)</td>")
        [void]$html.Append("<td>$($user.DaysSinceLastLogon)</td>")
        [void]$html.Append("</tr>")
    }

    [void]$html.Append("</tbody></table></div>")

    if ($truncated) {
        [void]$html.Append('<p style="color:#999;">Showing first 500 users. Full data is available in the source CSV.</p>')
    }
}
else {
    [void]$html.Append('<h2>Active Directory — User Report</h2>')
    [void]$html.Append('<div class="empty-msg">No AD report data loaded. Provide an AD report CSV with -ADReportPath.</div>')
}

# ───── System Inventory Tables ─────
if ($inventoryData.Count -gt 0) {
    [void]$html.Append('<h2><i class="icon">&#128187;</i> System Inventory</h2>')

    foreach ($machine in $inventoryData) {
        $computerName = $machine.ComputerName
        $machineOS = if ($machine.OperatingSystem) { $machine.OperatingSystem.Caption } else { "Unknown" }
        $machineModel = if ($machine.Hardware) { "$($machine.Hardware.Manufacturer) $($machine.Hardware.Model)" } else { "Unknown" }
        $machineRAM = if ($machine.Hardware) { "$($machine.Hardware.TotalRAMGB) GB" } else { "Unknown" }
        $machineCPU = if ($machine.CPU) { $machine.CPU } else { "Unknown" }
        $machineSerial = if ($machine.Hardware) { $machine.Hardware.SerialNumber } else { "Unknown" }

        [void]$html.Append(@"
<div class="collapsible">
    <div class="collapsible-header" onclick="this.nextElementSibling.style.display=this.nextElementSibling.style.display==='none'?'block':'none'">
        <span>$computerName</span>
        <span style="font-size:12px;color:#666;">$machineModel — $machineOS</span>
    </div>
    <div class="collapsible-content" style="display:block;">
"@)

        # System details
        [void]$html.Append(@"
        <table>
            <tr><th colspan="2">System Information</th></tr>
            <tr><td style="width:200px;font-weight:600;">Computer Name</td><td>$computerName</td></tr>
            <tr><td style="font-weight:600;">Operating System</td><td>$machineOS</td></tr>
            <tr><td style="font-weight:600;">Model</td><td>$machineModel</td></tr>
            <tr><td style="font-weight:600;">Serial Number</td><td>$machineSerial</td></tr>
            <tr><td style="font-weight:600;">CPU</td><td>$machineCPU</td></tr>
            <tr><td style="font-weight:600;">RAM</td><td>$machineRAM</td></tr>
            <tr><td style="font-weight:600;">Last Boot</td><td>$(if($machine.OperatingSystem){$machine.OperatingSystem.LastBootUpTime}else{'N/A'})</td></tr>
            <tr><td style="font-weight:600;">Uptime</td><td>$(if($machine.OperatingSystem){$machine.OperatingSystem.UptimeDays.ToString() + ' days'}else{'N/A'})</td></tr>
        </table>
"@)

        # Disks
        if ($machine.LogicalDisks) {
            [void]$html.Append('<h3>Logical Disks</h3>')
            [void]$html.Append('<div style="overflow-x: auto;"><table>')
            [void]$html.Append('<thead><tr><th>Drive</th><th>Volume</th><th>Size (GB)</th><th>Free (GB)</th><th>Used (GB)</th><th>Usage</th><th>Status</th></tr></thead><tbody>')
            foreach ($disk in $machine.LogicalDisks) {
                $usedPercent = if ($disk.UsedPercent) { $disk.UsedPercent } else { 0 }
                [void]$html.Append("<tr>")
                [void]$html.Append("<td><strong>$($disk.Drive)</strong></td>")
                [void]$html.Append("<td>$($disk.VolumeName)</td>")
                [void]$html.Append("<td>$($disk.SizeGB)</td>")
                [void]$html.Append("<td>$($disk.FreeSpaceGB)</td>")
                [void]$html.Append("<td>$($disk.UsedSpaceGB)</td>")
                [void]$html.Append("<td>$(Get-DiskBarHTML -UsedPercent $usedPercent) $([math]::Round($usedPercent, 1))%</td>")
                [void]$html.Append("<td>$(Get-StatusBadge -Status $disk.Alert)</td>")
                [void]$html.Append("</tr>")
            }
            [void]$html.Append('</tbody></table></div>')
        }

        # Network
        if ($machine.NetworkAdapters) {
            [void]$html.Append('<h3>Network Adapters</h3>')
            [void]$html.Append('<div style="overflow-x: auto;"><table>')
            [void]$html.Append('<thead><tr><th>Description</th><th>IP Address</th><th>MAC</th><th>DHCP</th><th>DNS Servers</th></tr></thead><tbody>')
            foreach ($nic in $machine.NetworkAdapters) {
                [void]$html.Append("<tr>")
                [void]$html.Append("<td>$($nic.Description)</td>")
                [void]$html.Append("<td>$($nic.IPAddress)</td>")
                [void]$html.Append("<td>$($nic.MACAddress)</td>")
                [void]$html.Append("<td>$($nic.DHCPEnabled)</td>")
                [void]$html.Append("<td>$($nic.DNSServers)</td>")
                [void]$html.Append("</tr>")
            }
            [void]$html.Append('</tbody></table></div>')
        }

        # Software (top 10)
        if ($machine.InstalledSoftware -and $machine.InstalledSoftware.Count -gt 0) {
            $swCount = $machine.InstalledSoftware.Count
            $topSW = $machine.InstalledSoftware | Sort-Object Name | Select-Object -First 20
            [void]$html.Append("<h3>Installed Software ($swCount applications)</h3>")
            [void]$html.Append('<div style="overflow-x: auto;"><table>')
            [void]$html.Append('<thead><tr><th>Name</th><th>Publisher</th><th>Version</th></tr></thead><tbody>')
            foreach ($sw in $topSW) {
                [void]$html.Append("<tr><td>$($sw.Name)</td><td>$($sw.Publisher)</td><td>$($sw.Version)</td></tr>")
            }
            if ($swCount -gt 20) {
                [void]$html.Append("<tr><td colspan='3' style='color:#999;font-style:italic;'>... and $($swCount - 20) more applications</td></tr>")
            }
            [void]$html.Append('</tbody></table></div>')
        }

        [void]$html.Append('</div></div>')
    }
}
else {
    [void]$html.Append('<h2>System Inventory</h2>')
    [void]$html.Append('<div class="empty-msg">No inventory data available. Provide inventory files with -InventoryPath or target computers with -TargetComputers.</div>')
}

# Footer
[void]$html.Append((Get-HTMLFooter))

# Write HTML file
try {
    $outputDir = Split-Path -Path $HtmlOutput -Parent
    if ($outputDir -and -not (Test-Path -Path $outputDir)) {
        New-Item -ItemType Directory -Path $outputDir -Force | Out-Null
        Write-LogMsg -Level INFO -Message "Created output directory: $outputDir"
    }

    $html.ToString() | Out-File -FilePath $HtmlOutput -Encoding utf8
    Write-LogMsg -Level OK -Message "HTML report generated: $HtmlOutput"
    Write-LogMsg -Level INFO -Message "Report size: $([math]::Round((Get-Item $HtmlOutput).Length / 1KB, 2)) KB"
}
catch {
    Write-LogMsg -Level ERROR -Message "Failed to write HTML report: $($_.Exception.Message)"
    Stop-Transcript
    exit 1
}

# ───── Send Email (optional) ─────
if ($SendEmail) {
    Write-LogMsg -Level INFO -Message "Sending email report to: $($MailTo -join ', ')"
    try {
        $mailParams = @{
            From         = $MailFrom
            To           = $MailTo
            Subject      = "IT Dashboard — $CompanyName ($(Get-Date -Format 'yyyy-MM-dd'))"
            Body         = "Please find attached the IT dashboard report generated on $(Get-Date -Format 'yyyy-MM-dd HH:mm:ss').`n`nThis report was generated by the PowerShell Toolkit.`n`n-- IT Department"
            SmtpServer   = $SmtpServer
            Attachments  = $HtmlOutput
        }

        Send-MailMessage @mailParams
        Write-LogMsg -Level OK -Message "Email sent successfully"
    }
    catch {
        Write-LogMsg -Level ERROR -Message "Failed to send email: $($_.Exception.Message)"
    }
}

