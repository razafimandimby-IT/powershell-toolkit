function Get-StatusBadge {
    param([string]$Status)
    switch ($Status) {
        "OK"         { return '<span class="badge badge-ok">OK</span>' }
        "WARNING"    { return '<span class="badge badge-warn">WARNING</span>' }
        "CRITICAL"   { return '<span class="badge badge-critical">CRITICAL</span>' }
        "Active"     { return '<span class="badge badge-ok">Active</span>' }
        default      {
            if ($Status -match "Disabled|Locked|Expired|PasswordExpired") {
                return '<span class="badge badge-warn">' + $Status + '</span>'
            }
            return '<span class="badge badge-info">' + $Status + '</span>'
        }
    }
}

function Get-DiskBarHTML {
    param([double]$UsedPercent)
    $color = if ($UsedPercent -gt 90) { "red" } elseif ($UsedPercent -gt 80) { "orange" } else { "green" }
    return '<div class="disk-bar"><div class="disk-bar-fill ' + $color + '" style="width:' + [math]::Round($UsedPercent) + '%"></div></div>'
}

#endregion

#region Main Execution

# Start transcript
$logDir = Join-Path -Path (Split-Path -Path $PSScriptRoot -Parent) -ChildPath "Logs"
if (-not (Test-Path -Path $logDir)) {
    New-Item -ItemType Directory -Path $logDir -Force | Out-Null
}
$logFile = "Invoke-ITReport_$(Get-Date -Format 'yyyy-MM-dd_HH-mm-ss').log"
$logPath = Join-Path -Path $logDir -ChildPath $logFile
Start-Transcript -Path $logPath -Force | Out-Null

Write-LogMsg -Level INFO -Message "Starting IT Report generation"
Write-LogMsg -Level INFO -Message "Company: $CompanyName"

# Validate parameters
if ($SendEmail) {
    $missingParams = @()
    if (-not $SmtpServer) { $missingParams += '-SmtpServer' }
    if (-not $MailFrom) { $missingParams += '-MailFrom' }
    if (-not $MailTo) { $missingParams += '-MailTo' }
    if ($missingParams.Count -gt 0) {
        Write-LogMsg -Level WARN -Message "Email requested but missing parameters: $($missingParams -join ', '). Email will be skipped."
        $SendEmail = $false
    }
}

if (-not $ADReportPath -and -not $InventoryPath -and -not $TargetComputers) {
    Write-LogMsg -Level ERROR -Message "No data sources specified. Provide -ADReportPath, -InventoryPath, or -TargetComputers."
    Stop-Transcript
    exit 1
}

# Initialize data containers
$adData = $null
$inventoryData = @()
$reportSections = @()

# ───── SECTION 1: Active Directory Report ─────
if ($ADReportPath) {
    Write-LogMsg -Level INFO -Message "Processing AD report: $ADReportPath"
    try {
        $adData = Import-Csv -Path $ADReportPath
        Write-LogMsg -Level OK -Message "Loaded $($adData.Count) user records from AD report"
    }
    catch {
        Write-LogMsg -Level ERROR -Message "Failed to load AD report: $($_.Exception.Message)"
    }
}

# ───── SECTION 2: System Inventory ─────
if ($InventoryPath) {
    Write-LogMsg -Level INFO -Message "Loading inventory files from: $InventoryPath"
    $inventoryFiles = Get-ChildItem -Path $InventoryPath -Filter "*.json" -File
    Write-LogMsg -Level INFO -Message "Found $($inventoryFiles.Count) inventory file(s)"

    foreach ($invFile in $inventoryFiles) {
        try {
            $invData = Get-Content -Path $invFile.FullName -Raw | ConvertFrom-Json
            $inventoryData += $invData
            Write-LogMsg -Level OK -Message "  Loaded: $($invFile.Name)"
        }
        catch {
            Write-LogMsg -Level WARN -Message "  Failed to load $($invFile.Name): $($_.Exception.Message)"
        }
    }
}

# Run inventory against target computers directly
if ($TargetComputers) {
    $scriptRoot = $PSScriptRoot
    $inventoryScript = Join-Path -Path $scriptRoot -ChildPath "Get-SystemInventory.ps1"

    if (-not (Test-Path -Path $inventoryScript)) {
        Write-LogMsg -Level WARN -Message "Get-SystemInventory.ps1 not found at: $inventoryScript"
    }
    else {
        foreach ($computer in $TargetComputers) {
            Write-LogMsg -Level INFO -Message "Running inventory on: $computer"
            $tempOutput = Join-Path -Path $env:TEMP -ChildPath "Inventory_$computer.json"

            $invParams = @{
                ComputerName = $computer
                OutputPath   = $tempOutput
                IncludeSoftware = $true
            }
            if ($Credential) { $invParams['Credential'] = $Credential }

            try {
                & $inventoryScript @invParams
                if (Test-Path -Path $tempOutput) {
                    $invData = Get-Content -Path $tempOutput -Raw | ConvertFrom-Json
                    $inventoryData += $invData
                    Write-LogMsg -Level OK -Message "  Inventory completed for: $computer"
                }
            }
            catch {
                Write-LogMsg -Level WARN -Message "  Inventory failed for $computer : $($_.Exception.Message)"
            }
        }
    }
}

